#include "ft_printf.h"

int	ft_putstr(char	*str)
{
	int	i;

	i = 0;
	while (*str != '\0')
	{
		write(1, str, 1);
		i++;
		str ++;
	}
	return (i);
}

int	ft_putchar(char c)
{
	write(1, &c, 1);
	return (1);
}

#include "ft_printf.h"

int	is_ident(char c)
{
	if(c == '%' || c == 'd' || c == 'i' || c == 'p')
		return (1);
	if(c == 'c' || c == 's' || c == 'x' || c == 'X')
		return (1);
	return (0);
}
	
